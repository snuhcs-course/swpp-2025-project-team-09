class StartSessionActivity : AppCompatActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContentView(R.layout.activity_start_session)

        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main)) { v, insets ->
            val systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars())
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom)
            insets
        }

        setupButtonListener()


    }

    private fun navigateToVoiceSelect() {
        val intent = Intent(this, VoiceSelectActivity::class.java)
        startActivity(intent)
        finish()
    }

    private fun setupButtonListener() {
        val goReadingButton = findViewById<Button>(R.id.goReadingButton)
        goReadingButton.setOnClickListener {
            navigateToVoiceSelect()
        }
    }
}
